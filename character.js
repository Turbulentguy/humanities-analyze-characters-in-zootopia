const SENTIMENT_DECIMALS = 4;

const sentimentClass = (sentiment) => {
  if (sentiment === "Negative") {
    return "negative";
  }
  if (sentiment === "Neutral") {
    return "neutral";
  }
  return "positive";
};

const normalizeSentimentCategory = (score, category) => {
  if (category === "Positive" || category === "Negative" || category === "Neutral") {
    return category;
  }
  if (score > 0.05) {
    return "Positive";
  }
  if (score < -0.05) {
    return "Negative";
  }
  return "Neutral";
};

const parseCsvLine = (line) => {
  const values = [];
  let current = "";
  let inQuotes = false;

  for (let index = 0; index < line.length; index += 1) {
    const char = line[index];

    if (char === '"') {
      if (inQuotes && line[index + 1] === '"') {
        current += '"';
        index += 1;
      } else {
        inQuotes = !inQuotes;
      }
      continue;
    }

    if (char === "," && !inQuotes) {
      values.push(current);
      current = "";
      continue;
    }

    current += char;
  }

  values.push(current);
  return values;
};

const parseCsv = (text) => {
  const lines = text.split(/\r?\n/).filter((line) => line.trim().length);
  if (!lines.length) {
    return [];
  }

  const headers = parseCsvLine(lines[0]).map((header) => header.trim());
  return lines.slice(1).map((line) => {
    const values = parseCsvLine(line);
    const row = {};
    headers.forEach((header, index) => {
      row[header] = (values[index] ?? "").trim();
    });
    return row;
  });
};

const fetchCsv = async (path) => {
  const response = await fetch(path);
  if (!response.ok) {
    throw new Error(`Cannot load ${path}`);
  }
  return response.text();
};

const toCapitalizedWords = (text) =>
  text
    .split(" ")
    .map((part) => (part ? `${part[0].toUpperCase()}${part.slice(1).toLowerCase()}` : part))
    .join(" ");

const toSentenceStyle = (text) => {
  const compact = text.replace(/\s+/g, " ").trim();
  if (!compact) {
    return "";
  }

  const pronounFixed = compact.replace(/\bi\b/g, "I");

  return pronounFixed.replace(/(^\s*[a-z])|([.!?]\s+[a-z])/g, (match) => {
    const lastChar = match[match.length - 1];
    return `${match.slice(0, -1)}${lastChar.toUpperCase()}`;
  });
};

const formatSigned = (value) => `${value >= 0 ? "+" : ""}${value.toFixed(SENTIMENT_DECIMALS)}`;

const uniqueLines = (lines) => {
  const seen = new Set();
  return lines.filter((line) => {
    const normalized = line.replace(/\s+/g, " ").trim();
    if (!normalized || seen.has(normalized)) {
      return false;
    }

    seen.add(normalized);
    return true;
  });
};

const getCharacterNameFromQuery = () => {
  const params = new URLSearchParams(window.location.search);
  return params.get("name") || "";
};

const renderNotFound = (name) => {
  const title = document.getElementById("characterName");
  const insight = document.getElementById("characterInsight");
  if (title) {
    title.textContent = name ? `${name} not found` : "Character not selected";
  }
  if (insight) {
    insight.textContent = "Please open this page from a character card on the dashboard.";
  }
};

const renderProfile = (profile) => {
  const title = document.getElementById("characterName");
  const insight = document.getElementById("characterInsight");
  const dialogueCount = document.getElementById("dialogueCount");
  const dialogueShare = document.getElementById("dialogueShare");
  const sentimentCategory = document.getElementById("sentimentCategory");
  const sentimentScore = document.getElementById("sentimentScore");
  const keywordList = document.getElementById("keywordList");
  const sampleLines = document.getElementById("sampleLines");
  const image = document.getElementById("characterImage");
  const fallback = document.getElementById("characterFallback");

  if (title) {
    title.textContent = profile.name;
  }

  if (insight) {
    insight.textContent = profile.personalityInsight;
  }

  if (dialogueCount) {
    dialogueCount.textContent = String(profile.dialogues);
  }

  if (dialogueShare) {
    dialogueShare.textContent = `${((profile.dialogues / profile.totalDialogues) * 100).toFixed(1)}%`;
  }

  if (sentimentCategory) {
    sentimentCategory.textContent = profile.sentiment;
    sentimentCategory.className = sentimentClass(profile.sentiment);
  }

  if (sentimentScore) {
    sentimentScore.textContent = formatSigned(profile.sentimentScore);
    sentimentScore.className = sentimentClass(profile.sentiment);
  }

  if (keywordList) {
    keywordList.innerHTML = profile.keywords.map((keyword) => `<span>${toCapitalizedWords(keyword)}</span>`).join("");
  }

  if (sampleLines) {
    sampleLines.innerHTML = profile.samples.length
      ? profile.samples.map((line) => `<li>${line}</li>`).join("")
      : "<li>No dialogue samples found.</li>";
  }

  if (image && fallback) {
    if (profile.avatarPath) {
      image.src = profile.avatarPath;
      image.style.display = "block";
      fallback.style.display = "none";
      image.onerror = () => {
        image.style.display = "none";
        fallback.style.display = "grid";
        fallback.textContent = profile.avatar || "🎬";
      };
    } else {
      image.style.display = "none";
      fallback.style.display = "grid";
      fallback.textContent = profile.avatar || "🎬";
    }
  }
};

const init = async () => {
  const selectedName = getCharacterNameFromQuery();
  if (!selectedName) {
    renderNotFound("");
    return;
  }

  try {
    const [sentimentCsv, transcriptCsv] = await Promise.all([
      fetchCsv("character_sentiment_analysis.csv"),
      fetchCsv("zootopia_transcript_cleaned.csv")
    ]);

    const sentimentRows = parseCsv(sentimentCsv);
    const transcriptRows = parseCsv(transcriptCsv);

    const sentimentByName = new Map();
    sentimentRows.forEach((row) => {
      const name = (row.Character || "").trim();
      const score = Number(row.Sentiment);
      if (!name || Number.isNaN(score)) {
        return;
      }
      sentimentByName.set(name, {
        score,
        category: (row["Sentiment Category"] || "").trim()
      });
    });

    const dialogueRows = transcriptRows.filter((row) => (row.Character || "").trim().length);
    const totalDialogues = dialogueRows.length;

    const selectedDialogues = dialogueRows.filter((row) => (row.Character || "").trim() === selectedName);
    const dialogues = selectedDialogues.length;

    const sentiment = sentimentByName.get(selectedName);
    const presentation = characterPresentation[selectedName] || {};
    if (!dialogues || !sentiment) {
      renderNotFound(selectedName);
      return;
    }

    const profile = {
      name: selectedName,
      dialogues,
      totalDialogues,
      sentimentScore: sentiment.score,
      sentiment: normalizeSentimentCategory(sentiment.score, sentiment.category),
      avatar: presentation.avatar || "🎬",
      avatarPath: presentation.avatarPath || "",
      keywords: presentation.keywords || ["dialogue", "tone", "presence"],
      personalityInsight:
        presentation.personalityInsight || "This profile is generated directly from transcript and sentiment CSV data.",
      samples: uniqueLines(
        selectedDialogues
          .map((row) => toSentenceStyle(row.Dialogue || ""))
          .filter((line) => line.length)
      ).slice(0, 5)
    };

    renderProfile(profile);
  } catch (_error) {
    renderNotFound(selectedName);
  }
};

document.addEventListener("DOMContentLoaded", init);
